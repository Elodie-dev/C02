#include <stdio.h>

int	ft_str_is_printable(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (str[i])
	{
		if (str[i] <= 126 && str[i] >= 32)
			count++;
		i++;
	}
	if (count == i)
		return (1);
	else
		return (0);
}
