#include <stdio.h>
#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (str[i])
	{
		if (str[i] <= 57 && str[i] >= 48)
			count++;
		i++;
	}
	if (count == i)
		return (1);
	else
		return (0);
}
